package com.api.config.enums;

/**
 * Class Role
 *
 * Enum representing the roles of users within the application.
 * The roles are used for controlling access and defining permissions in the system.
 *
 * <p>Currently, there are two roles:</p>
 * <ul>
 *     <li><b>ADMIN</b> - Represents an administrator with full access to the system.</li>
 *     <li><b>USER</b> - Represents a regular user with limited access to the system.</li>
 * </ul>
 */
public enum Role {
    ADMIN, USER
}
